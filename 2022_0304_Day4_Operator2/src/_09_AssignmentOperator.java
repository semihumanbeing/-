
public class _09_AssignmentOperator {

	public static void main(String[] args) {
		
		// 대입연산자: = 
		// 복합대입연산자 : += -= *= /= &= %= ^= |= >>= <<= 
		
		int n;
		n = 10;
		
		n++; // n = n + 1
		
		n +=2; // n = n + 2
		
		System.out.println(n);
		
		// 대입연산자
		
		int x, y, z;
		x = y = z = 0;
		
		System.out.println(x + y + z);

	}

}
