package mymain;

public class _2_MyMain_multiDimentionalArray1 {

	public static void main(String[] args) {

		int[][] mm = new int[3][4];

		// 0행(1번째줄)

		mm[0][0] = 1;
		mm[0][1] = 2;
		mm[0][2] = 3;
		mm[0][3] = 4;

		// 1행(2번째줄)
		mm[1][0] = 5;
		mm[1][1] = 6;
		mm[1][2] = 7;
		mm[1][3] = 8;

		// 2행(3번째줄)
		mm[2][0] = 9;
		mm[2][1] = 10;
		mm[2][2] = 11;
		mm[2][3] = 12;

	}

}
